using System;

namespace Facade
{
    public class Circulo : IFormaGeometrica
    {
        private double pi = 3.14159;
        public int Raio { get; set; } = 2;
        public double CalcularArea()
        {
            return pi * (Raio * Raio);
        }

        public double CalcularPerimetro()
        {
            return 2 * pi * Raio;
        }

